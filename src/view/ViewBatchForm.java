    private void lblDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblDeleteMouseClicked
        DefaultTableModel dtm=(DefaultTableModel) tblBatch.getModel();
        if(tblBatch.getSelectedRowCount()>0){
            JOptionPane.showMessageDialog(this, "select only one row at time", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else if(tblBatch.getSelectedRowCount()==0)
        JOptionPane.showMessageDialog(this, "select one row at time", "Warning", JOptionPane.WARNING_MESSAGE);
        else{
            int  id = (int) dtm.getValueAt(tblBatch.getSelectedRow(), 0);
            String name =  (String) dtm.getValueAt(tblBatch.getSelectedRow(), 1);
            Timestamp date= (Timestamp) dtm.getValueAt(tblBatch.getSelectedRow(), 2);
            Batch sup=new Batch();
            sup.setB_id(id);
            sup.setTime(date);
            JFrame frame= new DeleteBatchForm(sup,name);
            frame.setVisible(true);
        }
        loadTable();

    }//GEN-LAST:event_lblDeleteMouseClicked

