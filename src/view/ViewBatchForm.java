    private void loadTable(){
        try {
            DefaultTableModel dtm=(DefaultTableModel) tblBatch.getModel();
            List<Batch> all = batController.getAll();
            for (Batch batch : all) {
                Object [] row={batch.getB_id(),supController.getSupplierByID(batch.getS_id()).getName(),batch.getTime()};
                dtm.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewBatchForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

